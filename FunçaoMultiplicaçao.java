public class FunçaoMultiplicaçao{
    public static double multiplicaçao(double a, double b){
        double resultado = a * b; 
        return resultado;
    }
public static void main(String[] args) {
    double resultadoFinal = multiplicaçao(4.5, 2.0);
    System.out.println("A multiplicação é: " + resultadoFinal);
}
}
